n1 = int(input('Informe o primeiro número: '))
n2 = int(input('Informe o segundo número: '))

soma = n1+n2
print(f'{n1}+{n2} = {soma}')

sub = n1-n2
print(f'{n1}-{n2} = {sub}')

mult = n1*n2
print(f'{n1} x {n2} = {mult}')

div = n1/n2
print(f'{n1} / {n2} = {div}')

expo = pow(n1,n2)
print(f'{n1} elevado {n2} = {expo}')

radi = pow(n1,1/n2)
print(f'{n1} raiz quadrada {n2} = {radi}')

